package chapter1.operator;

public class Main3 {
    public static void main(String[] args) {
        int x= 3;
        int y= 5;

        System.out.println("x가 y보다 큰가? "+(x>y));
        System.out.println("x가 y보다 작은가? "+(x<y));
        System.out.println("x와 y가 같은가? "+(x==y));
        System.out.println("x와 y가 다른가? "+(x!=y));
    }
}
